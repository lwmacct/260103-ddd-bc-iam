// Package captcha 定义图形验证码领域模型。
//
// 本包提供登录、注册等敏感操作的人机验证能力，用于：
//   - 防止暴力破解：限制自动化登录尝试
//   - 防止垃圾注册：阻止机器人批量注册账户
//   - 安全验证：敏感操作的二次确认
//
// 设计特点：
//   - 验证码为一次性使用，验证后立即失效
//   - 支持过期时间配置，默认 5 分钟
//   - 大小写不敏感，提升用户体验
//
// 存储机制：
// 验证码数据默认存储在内存中（[CommandRepository] 实现），
// 适用于单实例部署。分布式部署建议使用 Redis 实现。
