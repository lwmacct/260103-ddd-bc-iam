// Package stats 实现统计数据的应用层用例。
//
// 本包仅提供 Query Handler（统计数据只读）：
//
// # Query（读操作）
//
//   - [query.GetStatsHandler]: 获取系统统计概览
//
// # 统计指标
//
// 用户统计：
//   - 用户总数
//   - 活跃用户数
//   - 今日新增用户
//
// 角色统计：
//   - 角色总数
//   - 权限总数
//
// 系统统计：
//   - 菜单项数量
//   - 设置项数量
//   - 审计日志数量
//
// 性能考虑：
// 统计查询可能涉及多表聚合，建议在高并发场景下使用缓存。
// 可通过 Redis 缓存统计结果，设置合理的过期时间。
//
// 依赖注入：所有 Handler 通过 [bootstrap.Container] 注册。
package stats
